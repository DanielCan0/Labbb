    private void registerUser() {
        String username = jTextField1.getText().trim();
        String password = jTextField2.getText().trim();

        if (usernames.contains(username)) {
            JOptionPane.showMessageDialog(this, "Username ya existe. Volviendo al menu principal.");
            new Lab();  
            dispose();  
        } else if (password.length() != 5) {
            JOptionPane.showMessageDialog(this, "El password debe tener exactamente 5 caracteres.");
        } else {
            usernames.add(username);  
            JOptionPane.showMessageDialog(this, "Jugador registrado exitosamente.");
            new LabII ().setVisible(true); 
            dispose(); 
        }
    }
   
